package de.analytikjena.darwin.data;


public class Blankvalue {
	
		String  name;
		String dilution;
		String smallship;
		
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getDilution() {
			return dilution;
		}
		public void setDilution(String dilution) {
			this.dilution = dilution;
		}
		public String getSmallship() {
			return smallship;
		}

	public void setSmallship(String smallship) {
			this.smallship = smallship;
		}
}